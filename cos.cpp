// C++ program to demonstrate 
// example of tan() function. 
#include <iostream> 
#include <complex> 
using namespace std; 
  
int main () 
{ 
  complex<double> complexnumber (1, 0); 
   
   
  // use of tan() function for complex number 
  cout << "The tan of " << complexnumber << " is "
                     << tan(complexnumber) << endl; 
   
   
  return 0; 
} 
